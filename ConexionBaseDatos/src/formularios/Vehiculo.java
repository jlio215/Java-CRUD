     public void filtrar(String valor){
        String[] titulos= {"idVehiculo", "nombreFabricante", "precioUnitario"};
        String[] registros = new String[3];
        
        DefaultTableModel modelo = new DefaultTableModel (null,titulos);
        String sql= "SELECT * FROM Vehiculo WHERE idVehiculo LIKE '%"+valor+"%'";
        
        try {
            Statement statement= conn.createStatement();
            ResultSet rs= statement.executeQuery(sql);
            while (rs.next()){
                registros[0]=rs.getString("idVehiculo");
                registros[1]=rs.getString("nombreFabricante");
                registros[2]=rs.getString("precioUnitario");
                
                modelo.addRow(registros);
            }
            tablaVehiculo.setModel(modelo);
        }catch (Exception e){
             JOptionPane.showMessageDialog(null, "Error Mostrando Vehiculo"+ e.getMessage());
        }
        
    }
     
      public void Actualizar(){
        try{
            int filaSeleccionada = tablaVehiculo.getSelectedRow();
            String dao = (String)tablaVehiculo.getValueAt(filaSeleccionada,0);
                
            String sql = "UPDATE Vehiculo SET  nombreFabricante=?, precioUnitario=? WHERE idVehiculo=?";
            PreparedStatement statement = conn.prepareStatement(sql);
            statement.setString(1 , txtnombreFabricante.getText());
            statement.setString(2 , txtprecioUnitario.getText());
            statement.setString(3, dao);
            statement.execute();
            JOptionPane.showMessageDialog(null, "Vehiculo Editada");
            
            int rowsInserted = statement.executeUpdate();            
        }catch(Exception ex){
            JOptionPane.showMessageDialog(null, "Error Editando Vehiculo"+ ex);
        }
    }
    
