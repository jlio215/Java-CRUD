    public void eliminarRegistro(){
        int filaSeleccionada = tablaVehiculo.getSelectedRow();
        
        try{
            Statement statement = conn.createStatement();
            String sql = "DELETE FROM Vehiculo WHERE idVehiculo LIKE '%"+tablaVehiculo.getValueAt(filaSeleccionada,0)+"%'";
            int n = statement.executeUpdate(sql);
            if (n>=0){
                JOptionPane.showMessageDialog(null,"Vehiculo  Eliminado");
            }
        }catch (Exception e){
                JOptionPane.showMessageDialog(null,"Vehiculo no Eliminado"+e.getMessage());
        }
    }
