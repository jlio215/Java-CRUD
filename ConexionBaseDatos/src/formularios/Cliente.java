    public void mostrar(){
        String[] titulos= {"aliasCliente","nombres","apellidos","email","celular","password","fechaNacimiento"};
        String[] registros = new String[8];
        DefaultTableModel modelo = new DefaultTableModel (null,titulos);
        String sql= "SELECT * FROM Cliente";
        
        try {
            Statement statement= conn.createStatement();
            ResultSet rs= statement.executeQuery(sql);
            while (rs.next()){
                registros[0]=rs.getString("aliasCliente");
                registros[1]=rs.getString("nombres");
                registros[2]=rs.getString("apellidos");
                registros[3]=rs.getString("email");
                registros[4]=rs.getString("celular");
                registros[5]=rs.getString("password");
                registros[6]=rs.getString("fechaNacimiento");
                
                modelo.addRow(registros);
            }
            tablaCliente.setModel(modelo);
        }catch (Exception e){
             JOptionPane.showMessageDialog(null, "Error Mostrando Cliente"+ e.getMessage());
        }
    }
    
