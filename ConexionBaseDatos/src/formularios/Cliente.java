     public void Actualizar(){
        try{
            String sql = "UPDATE Cliente SET  nombres=?, apellidos=?, email=?, celular=?, password=?, fechaNacimiento=? WHERE aliasCliente=?";
            //int filaSeleccionada = tablaCliente.getSelectedRow();
            String dao = (String)txtAlias.getText();
                        
            PreparedStatement statement = conn.prepareStatement(sql);
            statement.setString(1 , txtNombres.getText());
            statement.setString(2 , txtApellidos.getText());
            statement.setString(3 , txtEmail.getText());
            statement.setDouble(4 , Double.parseDouble(txtCelular.getText()));
            statement.setString(5 , txtPassword.getText());
            statement.setString(6, txtFechaNacimiento.getText());
            
            statement.setString(7, dao);
            statement.execute();
            JOptionPane.showMessageDialog(null, "Cliente Editado");       
            
        }catch(Exception ex){
            JOptionPane.showMessageDialog(null, "No"+ex);
        }
     
    }
    public void eliminarRegistro(){
        String dao = txtAlias.getText();
        
        try{
            Statement statement = conn.createStatement();
            String sql = "DELETE FROM Cliente WHERE aliasCliente LIKE '%"+dao+"%'";
            int n = statement.executeUpdate(sql);
            if (n>=0){
                JOptionPane.showMessageDialog(null,"Cliente  Eliminado");
            }
        }catch (Exception e){
                JOptionPane.showMessageDialog(null,"Cliente no Eliminado"+e.getMessage());
        }
    }
    
